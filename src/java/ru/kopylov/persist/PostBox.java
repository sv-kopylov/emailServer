package ru.kopylov.persist;

import java.io.Serializable;
import java.util.List;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.ManyToOne;
import javax.persistence.OneToMany;

/**
 *
 * @author Сергей
 */
@Entity
public class PostBox implements Serializable {
    
    private String login;
    private String password;
    private String name;
    
    @OneToMany (mappedBy = "postBox")
    private List <LettersTable> thisBoxLetters;
    
  

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

   

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long id;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof PostBox)) {
            return false;
        }
        PostBox other = (PostBox) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "ru.kopylov.persist.PostBox[ id=" + id + " ]";
    }

    public List <LettersTable> getThisBoxLetters() {
        return thisBoxLetters;
    }

    public void setThisBoxLetters(List <LettersTable> thisBoxLetters) {
        this.thisBoxLetters = thisBoxLetters;
    }
    
}
